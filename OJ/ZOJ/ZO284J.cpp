#include<bits/stdc++.h>
using namespace std;
int main(){
	
	int x,i,n=1,ans;
	while(cin >>x){
		n=1;
		ans=0;
		for(i=0;i<35;i++){
			if(x==n)ans++;
			
			n*=2;
		}
		if(ans==0)cout<<"No"<<'\n';
		else cout<<"Yes"<<'\n';
	}
}
